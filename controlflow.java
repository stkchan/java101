public class controlflow {
    public static void main(String[] args) {
        int num1 = 7;

        if (num1 > 10) {
            System.out.println("num1 is > than 10");
        } else if (num1 > 5) {
            System.out.println("num1 is > than 5");
            
        } else {
            System.out.println("num1 is < than 10");
        }

    }
}
