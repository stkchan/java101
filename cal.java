public class cal {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 3;

        float summary = (float) (num1 + num2); // Casting type
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
        System.out.println(summary);
    }
}
