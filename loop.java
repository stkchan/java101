public class loop {
    public static void main(String[] args) {
        for (int i = 1; i < 10; i++) {
           System.out.println("Hello Mundo"); 
        }
         
        int num1 = 0;
        while(num1 < 10) {
            System.out.println("Hello Mr.Mundo");
            num1++;
        }

        int num2 = 0;
        do {
            System.out.println("Hello Mr.Mundo Senior");
            num2++;
        }   while (num2 < 3);
    }
}
